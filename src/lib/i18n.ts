export type Dictionary = (typeof dictionaries)[Locale];

export function normalizeLocalePreference(
  value: string | null | undefined,
): LocalePreference {
  if (localePreferences.includes(value as LocalePreference)) {
    return value as LocalePreference;
  }

  return "auto";
}

export function normalizeThemePreference(
  value: string | null | undefined,
): ThemePreference {
  if (themePreferences.includes(value as ThemePreference)) {
    return value as ThemePreference;
  }

  return "warm";
}

export function resolveLocale(
  preference: LocalePreference,
  acceptLanguage: string | null,
): Locale {
  if (preference !== "auto") {
    return preference;
  }

  for (const entry of acceptLanguage?.split(",") ?? []) {
    const tag = entry.split(";")[0]?.trim().toLowerCase();
    if (!tag) {
      continue;
    }

    if (tag.startsWith("zh")) {
      return "zh";
    }
    if (tag.startsWith("ja")) {
      return "ja";
    }
    if (tag.startsWith("en")) {
      return "en";
    }
  }

  return "en";
}

export function getDictionary(locale: Locale): Dictionary {
  return dictionaries[locale];
}
